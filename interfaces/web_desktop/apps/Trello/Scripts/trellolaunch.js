Application.run = function()
{
	
}
Application.receiveMessage = function( msg )
{
	if( msg.command && msg.command == 'launch' )
	{
		Application.settings = msg;
		Application.showTrello();
		console.log('we got this',msg);
	}
}
Application.showTrello = function()
{
	document.getElementById('mainFrame').setAttribute('src', Application.settings.trelloboard );
}